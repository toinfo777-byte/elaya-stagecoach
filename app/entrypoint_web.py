@app.post("/tg/webhook")
async def tg_webhook(request: Request) -> Response:
    if DISABLE_TG:
        return Response(status_code=200)

    try:
        data = await request.json()
        update = Update.model_validate(data)
        await dp.feed_update(bot, update)
        return Response(status_code=200)
    except Exception as e:
        # –õ–æ–≥–∏—Ä—É–µ–º, —à–ª—ë–º –≤ Sentry –∏ —É–≤–µ–¥–æ–º–ª—è–µ–º –∞–¥–º–∏–Ω–æ–≤
        log.exception("Webhook handler error")
        if SENTRY_DSN:
            sentry_sdk.capture_exception(e)

        await safe_admin_alert(
            "‚ö†Ô∏è <b>Webhook error</b>\n"
            f"<code>env={ENV} build={BUILD_MARK}</code>\n"
            f"Exception: <code>{type(e).__name__}</code>"
        )
        # Telegram –∂–¥—ë—Ç 200/4xx/5xx ‚Äî –≤–µ—Ä–Ω—ë–º 200, —á—Ç–æ–±—ã –Ω–µ –∑–∞—Å–æ—Ä—è—Ç—å pending_update_count
        return Response(status_code=200)

