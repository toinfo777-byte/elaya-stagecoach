    go_router = getattr(ep, "go_router", getattr(ep, "router"))
    log.info("entrypoints loaded: using %s", "go_router" if hasattr(ep, "go_router") else "router")

    # 5) –ü–æ–¥–∫–ª—é—á–µ–Ω–∏–µ —Ä–æ—É—Ç–µ—Ä–æ–≤ ‚Äî –±–µ–∑ help_router –∏ –±–µ–∑ common_guard
    dp.include_routers(
        go_router,         # –≤—Ö–æ–¥–Ω—ã–µ —Ç–æ—á–∫–∏
        mc_router,         # üé≠ –ú–∏–Ω–∏-–∫–∞—Å—Ç–∏–Ω–≥
        leader_router,     # üß≠ –ü—É—Ç—å –ª–∏–¥–µ—Ä–∞
        tr_router,         # üèãÔ∏è –¢—Ä–µ–Ω–∏—Ä–æ–≤–∫–∞ –¥–Ω—è
        r_progress.router,
        r_privacy.router,
        r_settings.router,
        r_extended.router,
        r_casting.router,
        r_apply.router,
        faq_router,        # ‚ùì FAQ / –ø–æ–º–æ—â—å (–∏–¥—ë—Ç –ø–æ—Å–ª–µ–¥–Ω–∏–º)
    )

    # 6) –∫–æ–º–∞–Ω–¥—ã
