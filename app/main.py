    # 4) –≤—Ö–æ–¥–Ω–æ–π —Ä–æ—É—Ç–µ—Ä entrypoints ‚Äî –Ω–∞–¥—ë–∂–Ω–æ —á–µ—Ä–µ–∑ importlib
